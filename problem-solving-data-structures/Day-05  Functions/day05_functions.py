# Function Definition
def greet():
    print("Welcome to PDSA")

# Function with Parameters
def display_name(name):
    print("Name:", name)

# Function with Return Value
def add(a, b):
    return a + b

# Function Calls
greet()

display_name("Saloni")

result = add(10, 20)

print("Sum =", result)