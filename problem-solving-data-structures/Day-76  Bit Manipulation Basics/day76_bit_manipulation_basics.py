a = 5

b = 3

print("a =", a)

print("b =", b)

print("\nAND Operation:")

print(a & b)

print("\nOR Operation:")

print(a | b)

print("\nXOR Operation:")

print(a ^ b)

print("\nNOT Operation:")

print(~a)

print("\nLeft Shift:")

print(a << 1)

print("\nRight Shift:")

print(a >> 1)