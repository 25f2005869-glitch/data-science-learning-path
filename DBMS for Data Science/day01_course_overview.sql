-- ==========================
-- DAY 01 : COURSE OVERVIEW
-- ==========================

-- What is Data?
-- Raw facts and figures.

-- Example:
-- Name = Saloni
-- Age  = 20

-- What is Information?
-- Processed data that provides meaning.

-- Example:
-- Average marks of a class.

-- What is a Database?
-- An organized collection of related data.

-- Examples:
-- Student Database
-- Library Database
-- Bank Database

-- What is DBMS?
-- DBMS (Database Management System) is software
-- used to create, manage, store, retrieve,
-- and manipulate databases.

-- Why do we need a DBMS?

-- 1. Reduce Data Redundancy
-- 2. Improve Data Consistency
-- 3. Data Security
-- 4. Data Sharing
-- 5. Backup and Recovery
-- 6. Efficient Data Retrieval

-- Examples of DBMS:
-- MySQL
-- PostgreSQL
-- Oracle
-- SQL Server
-- SQLite

-- Day 01 Summary:
-- Data -> Raw Facts
-- Information -> Processed Data
-- Database -> Collection of Related Data
-- DBMS -> Software for Managing Databases