-- =====================================
-- DAY 02 : FILE SYSTEM VS DBMS
-- =====================================

-- What is a File System?
-- A file system is a method of storing data
-- in separate files on a computer.

-- Examples:
-- student.txt
-- employee.xlsx
-- customer.csv

-- Problems in Traditional File Systems

-- 1. Data Redundancy
-- Same data may be stored multiple times.

-- Example:
-- Student information stored in
-- admission file and examination file.

-- 2. Data Inconsistency
-- Different files may contain different
-- values for the same data.

-- Example:
-- Address updated in one file but not
-- updated in another file.

-- 3. Difficult Data Access
-- Retrieving information from multiple
-- files can be time-consuming.

-- 4. Data Isolation
-- Data is scattered across different files.

-- 5. Security Problems
-- File systems provide limited security.

-- 6. Backup and Recovery Issues
-- Recovery after system failure is difficult.

-- What is DBMS?
-- DBMS is software that manages databases
-- efficiently and securely.

-- Advantages of DBMS

-- 1. Reduced Data Redundancy
-- 2. Improved Data Consistency
-- 3. Better Security
-- 4. Easy Data Sharing
-- 5. Backup and Recovery Support
-- 6. Faster Data Retrieval
-- 7. Centralized Data Management

-- File System vs DBMS

-- File System:
-- Data stored in separate files
-- High redundancy
-- Low security
-- Difficult maintenance

-- DBMS:
-- Data stored in database
-- Low redundancy
-- High security
-- Easy maintenance

-- Day 02 Summary

-- File System:
-- Collection of independent files

-- DBMS:
-- Software for managing databases

-- DBMS solves:
-- Data Redundancy
-- Data Inconsistency
-- Security Issues
-- Data Isolation Problems